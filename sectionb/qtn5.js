//Describe the forEach and map methods in JavaScript. How are they similar, and 
// //how do they differ?


// Both forEach and map are array methods in JavaScript used to iterate over an array
//  and execute a callback function for each element. 


//Differences

// 1. forEach Method
// Used to iterate over an array and perform an operation on each element.

// Does not return a new array; it only executes the provided function.

// Typically used when you want to perform side effects (e.g., logging, modifying the DOM, updating variables).



// 2. map Method
// Used to transform an array by applying a function to each element.

// Returns a new array with the transformed values.

// Useful when you want to create a modified version of the original array without changing it
